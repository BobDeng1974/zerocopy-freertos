void vAssertCalled( unsigned long ulLine, const char * const pcFileName )
{
	/* Parameters are not used. */
	( void ) ulLine;
	( void ) pcFileName;

	taskDISABLE_INTERRUPTS();
	__asm volatile( "int $3" );

	taskENABLE_INTERRUPTS();

}
